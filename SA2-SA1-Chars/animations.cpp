//Since SA1 skeleton isn't compatible with SA2 Anim, we replace those with the falling anim
void AddAnimFailSafe()
{
	AnimationIndex* charAnimR = getCharAnim_r();

	for (uint16_t i = 0; i < LengthOfArray(SonicAnimationList_r); i++)
	{
		if (!charAnimR[i].Animation) //assign "fall" animation to every SA2 animations that aren't replaced
		{
			SetCharacterAnim(i, charAnimR[15].Count, charAnimR[15].Animation);
		}
	}
}

void LoadCharacters_r()
{
	auto original = reinterpret_cast<decltype(LoadCharacters_r)*>(LoadCharacter_t->Target());
	original();
	PrintDebug("SA1 Char: Add failsafe Anim!\n");

	AddAnimFailSafe();
	return;
}


void PatchAnimations()
{
	if (sonic) 
	{
		WriteData((AnimationInfo**)0x716F0A, SonicAnimationList_r);
		WriteData((NJS_MOTION**)0x43EE1D, (NJS_MOTION*)0x1740C8C); //swap victory cam with knux to fit sa1 more
	}

	if (miles)
	{
		WriteData((AnimationInfo**)0x74CFD7, TailsAnimationList_R);
	}

	if (knux)
	{
		WriteData((AnimationInfo**)0x72821F, KnucklesAnimationList_r);
	}

	LoadCharacter_t = new Trampoline((int)LoadCharacters, (int)LoadCharacters + 0x6, LoadCharacters_r);

}
